package day01_27_10;

public class Q2_ozelIsaretler {

	public static void main(String[] args) {
		/*
        \n (or) \r: starts from a new line : metni bir alt satýrdan itibaren yazdýrýr.
                \t:  horizontal tab : metni yatayda 1 tab miktarý kaydýrýr.
                \\: prints a back slash :\ (ters slash) yazdýrýr
                \': prints single quote :' tek týrnak yazdýrýr.
                \": prints double quote :"" Çift týrnak yazdýrýr.
       */

		// Soru-1 : konsola "Hello ", "World \ /" seklinde yazdýrýnýz

		System.out.println("_______________Soru-1_____________\n");
		System.out.println("\"Hello\"" + ", " + "\"World" + "  \\" + "  /\"");

		// Soru-2 : Pazartesi kelimesini her harfini ayrý bir satira gelecek sekilde
		// yazdýrýnýz.

		System.out.println("_______________Soru-2_____________\n");
		System.out.println("P\nA\nZ\nA\nR\nT\nE\nS\nI");

		// Soru-3 : "Java" ile hayat cok 'afilli' yazdýrýnýz

		System.out.println("_______________Soru-3_____________\n");
		System.out.println("\"Java\" ile hayat cok \'afilli\'");

		/*
		 * Soru-4 : "Basari" gayrete 'asiktir' yazdýrýnýz sonraki
		 * "TechProEd ile java cok kolay ..." ifadesi 3 satýr sonra ve satýr basýndan 1
		 * tab ilerideyazýlsýn.
		 */

		System.out.println("_______________Soru-4_____________\n");
		System.out.println("\"Basari\" gayrete\n\'asiktir\' \n\n\n\t \"TechProEd ile java cok kolay ...\"");

		
		
	}

}
