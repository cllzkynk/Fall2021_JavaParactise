package day01_27_10_Odevler;

public class Q1_Max_Min_Value {

	public static void main(String[] args) {

		/* Primitive data type
		 * boolean, char, byte, short, integer,long, double, float  */ 
		 
		//byte, short, integer,long, double, float veri tiplerinin max ve min degerlerini yazdiriniz.
		
		
		
		
		Byte byteMinSayi = Byte.MIN_VALUE;
		Short shortMinSayi= Byte.MIN_VALUE;
		Integer integerMinSayi= Integer.MIN_VALUE;
		Long longMinSayi= Long.MIN_VALUE;
		Float floatMinSayi= Float.MIN_VALUE;
		Double doubleMinSayi=Double.MIN_VALUE;
		
		
		Byte byteMaxSayi = Byte.MAX_VALUE;
		Short shortMaxSayi= Byte.MAX_VALUE;
		Integer integerMaxSayi= Integer.MAX_VALUE;
		Long longMaxSayi= Long.MAX_VALUE;
		Float floatMaxSayi= Float.MAX_VALUE;
		Double doubleMaxSayi=Double.MAX_VALUE;
		
		System.out.println("Byte    degiskeninin en kucuk degeri    : "+byteMinSayi+" , "+"\n\t\t\tEn buyuk degeri : "+byteMaxSayi);
		System.out.println("Short   degiskeninin en kucuk degeri    : "+shortMinSayi+" , "+"\n\t\t\tEn buyuk degeri : "+shortMaxSayi);
		System.out.println("Integer degiskeninin en kucuk degeri    : "+integerMinSayi+" , "+"\n\t\t\tEn buyuk degeri : "+integerMaxSayi);
		System.out.println("Long    degiskeninin en kucuk degeri    : "+longMinSayi+" , "+"\n\t\t\tEn buyuk degeri : "+longMaxSayi);
		System.out.println("Float   degiskeninin en kucuk degeri    : "+floatMinSayi+" , "+"\n\t\t\tEn buyuk degeri : "+floatMaxSayi);
		System.out.println("Double  degiskeninin en kucuk degeri    : "+doubleMinSayi+" , "+"\n\t\t\tEn buyuk degeri : "+doubleMaxSayi);
		
		
	}
	


}
